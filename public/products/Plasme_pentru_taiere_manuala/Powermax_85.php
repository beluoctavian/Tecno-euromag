<h4>Avantaje sursa de alimentare</h4>
<p>
	<ul>
		<li>Tehnologia Smart Sense regleaza automat presiunea   de gaz in functie de modul de taiere si de lungimea pistoletului pentru   performante optime</li>
		<li>Pistolete FastConnect, interfata CNC,  si patru moduri de lucru creste versatilitatea pentru folosirea manuala si mecanizata</li>
		<li>Panou de control simplificat, cu afisaj LCD pentru operare usoara</li>
	</ul>
</p>

<h4>Avantajele pistoletului</h4>
<p>
	<ul>
		<li>Seria Duramax  15° si 75° pistolete manuale - permite utlilizatorului sa selecteze usor scula potrivita de   lucru. Manerul este mult mai rezistent si dureaza cel putin de cinci ori   mai mult in testele de impact decat pistoletele anterioare</li>
		<li>Pistoletele Duramax standard si cu teava scurta permit o gama variata de   aplicatii mecanizate pe mese X-Y, sisteme de cale, conducte si sisteme   robotizate de taiere</li>
		<li>Duzele Conical Flow cresc densitatea de energie a arcului pentru taieri superioare cu zgura putina</li>
	</ul>
</p>

