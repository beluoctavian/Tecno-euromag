<p>Powermax30 este sistemul de varf pentru taierea de   metale cu o grosime de pana la 10 mm. Este proiectat si construit pentru   folosire in medii dificile, totusi este extrem de compact si cantareste   doar 9 kg. Caseta de transport si cureaua de umar optionale usureaza si   mai mult transportarea sistemului. </p>

<h4>Capacitate de taiere pistolet manual</h4>
<p><strong>Recomandata:</strong> pana la 6 mm la viteze de taiere de 838 mm pe minut </p>
<p><strong>Maximala:</strong> pana la 10 mm la viteze de taiere de 381 mm pe minut </p>
<p><strong>Separare:</strong> pana la 12 mm </p>

<h4>Caracteristici sursa de alimentare</h4>
<p>
	<ul>
		<li>Putere de taiere 30 amperi, 2,5 kW</li>
		<li>Circuit Auto-voltage</li>
		<li>Tensiune circuit de rulare in gol amplificat</li>
		<li>Controler arc pilot electronic, activ</li>
		<li>Compartiment de stocare pentru consumabile</li>
		<li>Regulator de aer presetat din fabrica</li>
		<li>Modelele CSA includ un conector de 240V/20A cu adaptoare pentru circuite de 120V/15A si 240V/50A</li>
	</ul>
</p>

