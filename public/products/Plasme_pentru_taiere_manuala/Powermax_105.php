<h4>Productivitate maxima</h4>
<p>
	<ul>
		<li>Viteza mare de taiere: de 3 ori mai rapid decat taiere oxigaz, la otel de 12 mm</li>
		<li>Taierea superioara si calitatea in craituire, inseamna mai putin timp petrecut la netezirea marginilor</li>
	</ul>
</p>

<h4>Usor de folosit in taiere si craituire</h4>
<p>
	<ul>
		<li>Tehnologia Smart Sense stabileste in mod automat presiunea de aer corecta bazata pe lungime pistoletului si a modului de taiere</li>
		<li>Pistolete manuale, robotizate pentru o mai mare versatilitae si usurinta in folosire</li>
		<li>Toleranta larga de tensiuni imbunatateste performanta pe generatorele cu motor</li>
		<li>Scutul   de protectie imbunatatit ajuta la reducerea depunerilor de zgura si   permite miscarea mai lina a pistoletului pe piesa de taiat pentru a   obtine o calitate de taiere mai buna</li>
	</ul>
</p>

<h4>Construit si testat pentru a rezista celor mai dure conditii</h4>
<p>
	<ul>
		<li>Pistoletele Duramax sunt proiectate pentru un impact mare de caldura si rezistenta</li>
		<li>Operatiuni de intretinere mai putine pentru o functionare cat mai lunga – mentine sistemul in functionare</li>
		<li>Tehnologia patentata de amorsare SpringStart, asigura un numar de aprinderi consistent si a functionare fiabila a pistoletului</li>
	</ul>
</p>

<h4>Costuri de operare scazute</h4>
